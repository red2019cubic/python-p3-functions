def greet_programmer():
    return print("Hello, programmer!")


def greet(name):
    return print(f"Hello, {name}!")


def greet_with_default(name="programmer"):
    return print(f"Hello, {name}!")


def add(num1, num2):
    return num1 + num2


def halve(number):
    return number / 2
